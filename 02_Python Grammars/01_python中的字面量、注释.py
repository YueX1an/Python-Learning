# python中的变量
# 定义一个变量
money = 50
# 通过print语句，输出变量记录的内容
print("钱包还有:", money)
# 买了一个冰激凌，花费10元
money = money - 10
print("买了冰激凌花费10元，还剩：", money, "元")


# 变量
message = "hello python world"
print(message)
message = "python crash course world"
print(message)
# 变量只能包含字母，下划线和数字，可以由下划线和字母开头，但不能以数字开头，如message_1是允许的，但1_message不被允许
# 变量名中不能出现空格，但可以用下划线隔开字母和字母，如message_greeting
# 不能用python关键字和函数作为变量名，如print
# 变量名应简短和具有描述性
# 慎用小写字母i和大写字母o，容易被误认为0和1
















